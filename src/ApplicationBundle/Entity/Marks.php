<?php

namespace ApplicationBundle\Entity;

/**
 * Marks
 */
class Marks
{
    /**
     * @var string
     */
    private $type;

    /**
     * @var integer
     */
    private $mark;

    /**
     * @var integer
     */
    private $id;

    /**
     * @var \ApplicationBundle\Entity\MarkingScheme
     */
    private $markingScheme;


    /**
     * Set type
     *
     * @param string $type
     *
     * @return Marks
     */
    public function setType($type)
    {
        $this->type = $type;

        return $this;
    }

    /**
     * Get type
     *
     * @return string
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * Set mark
     *
     * @param integer $mark
     *
     * @return Marks
     */
    public function setMark($mark)
    {
        $this->mark = $mark;

        return $this;
    }

    /**
     * Get mark
     *
     * @return integer
     */
    public function getMark()
    {
        return $this->mark;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set markingScheme
     *
     * @param \ApplicationBundle\Entity\MarkingScheme $markingScheme
     *
     * @return Marks
     */
    public function setMarkingScheme(\ApplicationBundle\Entity\MarkingScheme $markingScheme = null)
    {
        $this->markingScheme = $markingScheme;

        return $this;
    }

    /**
     * Get markingScheme
     *
     * @return \ApplicationBundle\Entity\MarkingScheme
     */
    public function getMarkingScheme()
    {
        return $this->markingScheme;
    }
}
